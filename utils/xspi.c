int SPITX_Code(int fd,uint8_t fun,uint8_t *SPI_Tx,uint8_t *SPI_Rx)
{
	uint16_t len = 0,CRC_Val = 0;
	uint8_t CRC_HI = 0,CRC_LO = 0;
	int i = 0;
	//uint8_t *p;

	switch(fun)
	{
		case RD_STATUS:
		{
			SPI_Tx[0] = (uint8_t)(BASE_ADDRESS >> 16);
			SPI_Tx[1] = (uint8_t)(BASE_ADDRESS >> 8);
			SPI_Tx[2] = (uint8_t)(BASE_ADDRESS & 0xff);
			SPI_Tx[3] = 0x80;

			len = 4+53;
			i = SPItransfer(fd, SPI_Tx, SPI_Rx, len);
			if(i < 0)
			return -1;

			//p = SPI_Rx;
			CRC_Val = CRC16(&SPI_Rx[4], 51);
			CRC_LO  = (uint8_t)(CRC_Val & 0xff);
			CRC_HI  = (uint8_t)(CRC_Val >> 8);
			if((CRC_LO != SPI_Rx[55]) || (CRC_HI != SPI_Rx[56]))
			{
				logger_hexbuf("SPI_RX", SPI_Rx, 57);
				logger_info("CRC ERR:crc:%02x %02x SPI_Rx:%02x %02x ",CRC_LO,CRC_HI,SPI_Rx[55],SPI_Rx[56]);
				return -1;
			}
			logger_hexbuf("STATUS RX",&SPI_Rx[4],53);

			break;
		}
		case WR_STATUS:
		{
			SPI_Tx[0] = (uint8_t)(BASE_ADDRESS >> 16);
			SPI_Tx[1] = (uint8_t)(BASE_ADDRESS >> 8);
			SPI_Tx[2] = (uint8_t)(BASE_ADDRESS & 0xff);
			SPI_Tx[3] = 0x00;

			for(i = 0; i < 51 ; i++)
			{
				SPI_Tx[4+i] = 0xfc;
			}
			CRC_Val = CRC16(&SPI_Rx[4], 51);
			SPI_Tx[55]  = (uint8_t)(CRC_Val & 0xff);
			SPI_Tx[56]  = (uint8_t)(CRC_Val >> 8);

			len = 4+53;
			i = SPItransfer(fd, SPI_Tx, SPI_Rx, len);
			if(i < 0)
			return -1;

			logger_hexbuf("STATUS SET",&SPI_Tx[4],53);

			break;
		}
		case ED_STATUS:
		{
			break;
		}
		default:break;
	}
	return 1;
}



