/*------------------------------------------------------------------------------------------------
 * int SPI_Configration(int fd)
 *------------------------------------------------------------------------------------------------ */
int SPI10_Configration(void)
{
	int ret = 0;
	int fd  = 0;
	static const char *device = "/dev/spidev1.0";
	static uint32_t mode;
	static uint8_t bits = 8;
	static uint32_t speed = 4000000;


	fd = open(device, O_RDWR);
	if (fd < 0){
		logger_error("LinuxSPI: can't open device");
		return -1;
	}
	//mode |= SPI_CPHA;
	//mode |= SPI_CPOL;

	/*
	 * spi mode
	 */
	ret = ioctl(fd, SPI_IOC_WR_MODE32, &mode);
	if (ret == -1)
	{
		logger_error("LinuxSPI: can't set spi mode");
	}

	ret = ioctl(fd, SPI_IOC_RD_MODE32, &mode);
	if (ret == -1){
		logger_error("LinuxSPI: can't get spi mode");
		return -1;
	}
	/*
	 * bits per word
	 */
	ret = ioctl(fd, SPI_IOC_WR_BITS_PER_WORD, &bits);
	if (ret == -1){
		logger_error("LinuxSPI: can't set bits per word");
		return -1;
	}

	ret = ioctl(fd, SPI_IOC_RD_BITS_PER_WORD, &bits);
	if (ret == -1){
		logger_error("LinuxSPI: can't get bits per word");
		return -1;
	}

	/*
	 * max speed hz
	 */
	ret = ioctl(fd, SPI_IOC_WR_MAX_SPEED_HZ, &speed);
	if (ret == -1){
		logger_error("LinuxSPI: can't set max speed hz");
		return -1;
	}

	ret = ioctl(fd, SPI_IOC_RD_MAX_SPEED_HZ, &speed);
	if (ret == -1){
		logger_error("LinuxSPI: can't get max speed hz");
		return -1;
	}

	logger_info("LinuxSPI: spi mode: 0x%x", mode);
	logger_info("LinuxSPI: bits per word: %d", bits);
	logger_info("LinuxSPI: max speed: %d Hz (%d KHz)", speed, speed/1000);

	return fd;
}


